"""
glycosignal.report
==================

Summary report generation for CGM data.

Produces an HTML report containing:
  - Summary statistics table
  - Time-in-range metrics table
  - Risk index table
  - AGP plot (embedded as base64 PNG)
  - Glucose time series plot (embedded as base64 PNG)

Usage
-----
    >>> from glycosignal import report
    >>> report.generate_summary_report(df, output_path="report.html")
"""

from __future__ import annotations

import base64
import io
from pathlib import Path

import pandas as pd

from . import metrics, plotting
from .schemas import COL_GLUCOSE, COL_TIMESTAMP
from .utils import require_columns, require_dataframe


# ─────────────────────────────────────────────────────────────────────────────
# Plot helpers
# ─────────────────────────────────────────────────────────────────────────────

def _fig_to_base64(fig) -> str:
    """Encode a matplotlib figure as a base64 PNG string."""
    buf = io.BytesIO()
    fig.savefig(buf, format="png", dpi=120, bbox_inches="tight")
    buf.seek(0)
    b64 = base64.b64encode(buf.read()).decode("utf-8")
    buf.close()
    try:
        import matplotlib.pyplot as plt
        plt.close(fig)
    except Exception:
        pass
    return b64


def _make_plots_b64(df: pd.DataFrame) -> tuple[str, str]:
    """Generate AGP and time series plots and return as base64 strings."""
    try:
        import matplotlib
        matplotlib.use("Agg")
        fig_ts, _ = plotting.plot_glucose_timeseries(df, show_tir_bands=True)
        ts_b64 = _fig_to_base64(fig_ts)

        fig_agp, _ = plotting.plot_agp(df)
        agp_b64 = _fig_to_base64(fig_agp)

        return ts_b64, agp_b64
    except Exception:
        return "", ""


