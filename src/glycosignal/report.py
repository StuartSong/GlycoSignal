def _table_html(rows: list[tuple[str, str, str]], headers: tuple[str, str, str]) -> str:
    """Render a simple two-column HTML table."""
    rows_html = "\n".join(
        f"<tr><td>{label}</td><td class='metric-value'>{value}</td><td>{note}</td></tr>"
        for label, value, note in rows
    )
    return (
        f"<table><thead><tr>"
        f"<th>{headers[0]}</th><th>{headers[1]}</th><th>{headers[2]}</th>"
        f"</tr></thead><tbody>{rows_html}</tbody></table>"
    )


def _img_tag(b64: str) -> str:
    if not b64:
        return "<p><em>Plot unavailable (matplotlib error)</em></p>"
    return f'<img class="plot" src="data:image/png;base64,{b64}" alt="plot" />'


# ─────────────────────────────────────────────────────────────────────────────
# Public API
# ─────────────────────────────────────────────────────────────────────────────

def generate_summary_report(
    df: pd.DataFrame,
    output_path: str | Path | None = None,
    format: str = "html",
    subject: str | None = None,
) -> str:
    """Generate a CGM summary report and optionally save it to disk.

    Parameters
    ----------
    df : pd.DataFrame
        CGM data with ``Timestamp`` and ``Glucose`` columns.
    output_path : str | Path | None
        If provided, the report HTML is written to this file.
    format : str
        Output format.  Currently only ``"html"`` is supported.
    subject : str | None
        Optional subject label shown in the report title.

    Returns
    -------
    str
        The generated HTML string.

    Raises
    ------
    ValueError
        If *format* is not ``"html"``.
    """
    require_dataframe(df, "df")
    require_columns(df, [COL_TIMESTAMP, COL_GLUCOSE])

    if format != "html":
        raise ValueError(f"Only format='html' is currently supported, got {format!r}.")

    from datetime import datetime

    ts = pd.to_datetime(df[COL_TIMESTAMP], errors="coerce")
    gl = pd.to_numeric(df[COL_GLUCOSE], errors="coerce")
    valid = ts.notna() & gl.notna()
    ts_valid = ts[valid]
    n_readings = int(valid.sum())

    period_start = ts_valid.min().strftime("%Y-%m-%d") if n_readings else "N/A"
    period_end = ts_valid.max().strftime("%Y-%m-%d") if n_readings else "N/A"
    duration_days = (
        (ts_valid.max() - ts_valid.min()).total_seconds() / 86400
        if n_readings >= 2 else 0.0
    )

    # Compute metrics
    d = df[[COL_TIMESTAMP, COL_GLUCOSE]].copy()
    from .schemas import prepare
    prep = prepare(d)

    def _fmt(v, digits=1):
        if v is None or (isinstance(v, float) and pd.isna(v)):
            return "N/A"
        return f"{v:.{digits}f}"

    stats_rows = [
        ("Mean glucose", _fmt(metrics.mean_glucose(prep)), "mg/dL"),
        ("Median glucose", _fmt(metrics.median_glucose(prep)), "mg/dL"),
        ("Min glucose", _fmt(metrics.min_glucose(prep)), "mg/dL"),
        ("Max glucose", _fmt(metrics.max_glucose(prep)), "mg/dL"),
        ("Q1 (25th %ile)", _fmt(metrics.q1_glucose(prep)), "mg/dL"),
        ("Q3 (75th %ile)", _fmt(metrics.q3_glucose(prep)), "mg/dL"),
        ("SD", _fmt(metrics.sd(prep)), "mg/dL"),
        ("CV", _fmt(metrics.cv(prep)), "%"),
        ("J-index", _fmt(metrics.j_index(prep)), ""),
        ("MAGE", _fmt(metrics.mage(prep)), "mg/dL"),
    ]

    tir_rows = [
        ("Time in Target 70–180 mg/dL", _fmt(metrics.time_in_range_percent(prep, 70, 180)), "%"),
        ("Time in Tight 70–140 mg/dL", _fmt(metrics.time_in_range_percent(prep, 70, 140)), "%"),
        ("Time Below 70 mg/dL (L1 Hypo)", _fmt(metrics.time_below_range_percent(prep, 70)), "%"),
        ("Time Below 54 mg/dL (L2 Hypo)", _fmt(metrics.time_below_range_percent(prep, 54)), "%"),
        ("Time Above 180 mg/dL (L1 Hyper)", _fmt(metrics.time_above_range_percent(prep, 180)), "%"),
        ("Time Above 250 mg/dL (L2 Hyper)", _fmt(metrics.time_above_range_percent(prep, 250)), "%"),
    ]

    risk_rows = [
        ("LBGI (Low BG Index)", _fmt(metrics.lbgi(prep)), "higher = more hypo risk"),
        ("HBGI (High BG Index)", _fmt(metrics.hbgi(prep)), "higher = more hyper risk"),
        ("ADRR", _fmt(metrics.adrr(prep)), "daily risk range"),
        ("GRI (Glucose Risk Index)", _fmt(metrics.gri(prep)), "0–100; higher = more risk"),
    ]

    stats_table = _table_html(stats_rows, ("Metric", "Value", "Unit"))
    tir_table = _table_html(tir_rows, ("Metric", "Value", "Unit"))
    risk_table = _table_html(risk_rows, ("Metric", "Value", "Note"))

    # Generate plots
    ts_b64, agp_b64 = _make_plots_b64(df)
    ts_plot = _img_tag(ts_b64)
    agp_plot = _img_tag(agp_b64)

    html = _HTML_TEMPLATE.format(
        generated_at=datetime.now().strftime("%Y-%m-%d %H:%M"),
        period_start=period_start,
        period_end=period_end,
        duration_days=duration_days,
        n_readings=n_readings,
        stats_table=stats_table,
        tir_table=tir_table,
        risk_table=risk_table,
        ts_plot=ts_plot,
        agp_plot=agp_plot,
    )

    if output_path is not None:
        output_path = Path(output_path)
        output_path.parent.mkdir(parents=True, exist_ok=True)
        output_path.write_text(html, encoding="utf-8")

    return html
